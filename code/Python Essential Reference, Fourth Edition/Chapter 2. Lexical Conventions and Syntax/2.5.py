# 2.5 Containers

a = [ 1, 3.4, 'hello' ] # A list
b = ( 10, 20, 30 )      # A tuple
c = { 'a':3,'b':42}     # A dictionary

print('a = ', a)
print('b = ', b)
print('c = ', c)

d = [ 1,
      3.4,
      'hello',
    ]
print('d = ',d)
